#1.	Write a program to demonstrate basic data type in python. 

# Integer type
integer_var = 10
print("Integer:", integer_var)
print("Data type of integer_var:", type(integer_var))

# Float type
float_var = 3.14
print("\nFloat:", float_var)
print("Data type of float_var:", type(float_var))

# String type
string_var = "Hello, Python!"
print("\nString:", string_var)
print("Data type of string_var:", type(string_var))

# Boolean type
bool_var = True
print("\nBoolean:", bool_var)
print("Data type of bool_var:", type(bool_var))

# List type (Mutable)
list_var = [1, 2, 3, 4, 5]
print("\nList:", list_var)
print("Data type of list_var:", type(list_var))

# Tuple type (Immutable)
tuple_var = (1, 2, 3, 4, 5)
print("\nTuple:", tuple_var)
print("Data type of tuple_var:", type(tuple_var))

# Dictionary type
dict_var = {"name": "Alice", "age": 25}
print("\nDictionary:", dict_var)
print("Data type of dict_var:", type(dict_var))

# Set type (Unique unordered elements)
set_var = {1, 2, 3, 4, 5}
print("\nSet:", set_var)
print("Data type of set_var:", type(set_var))
